package first;

public class Varf {
	protected double vfa, vfb;

	public Varf() {
		vfa = 0;
		vfb = 0;
	}

	public Varf(double x, double y) {
		vfa = x;
		vfb = y;
	}

	public void setVfa(double vfa) {
		this.vfa = vfa;
	}

	public void setVfb(double vfb) {
		this.vfb = vfb;
	}

}